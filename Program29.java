//29. Write a program in Java to compute quotient and remainder.

class Program29{
      public static void main(String []args){
         int dividend=25 , divisor=3 , quotient ,remainder;
         System.out.println("The Dividend is : "+dividend);
         System.out.println("The Dividend is : "+divisor);
         quotient = dividend / divisor ;
         remainder = dividend % divisor ;
         System.out.println("The Quotient of Division is : "+quotient);
         System.out.println("The  Remainder of Division is : "+remainder); 
 
  }
}